// Function to add two numbers
function add(a, b) {
    return a + b;
}

// Function to subtract two numbers
function subtract(a, b) {
    return a - b;
}

// Example usage
let num1 = 10;
let num2 = 5;

let additionResult = add(num1, num2);
let subtractionResult = subtract(num1, num2);

console.log("Addition:", additionResult);       // Output: Addition: 15
console.log("Subtraction:", subtractionResult); // Output: Subtraction: 5
